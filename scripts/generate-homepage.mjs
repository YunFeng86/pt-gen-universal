import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

const sourcePath = fileURLToPath(new URL('../index.html', import.meta.url));
const outputPath = fileURLToPath(new URL('../src/runtime/page.static.ts', import.meta.url));
const checkOnly = process.argv.includes('--check');

function renderOutput(html) {
  const base64 = Buffer.from(html, 'utf8').toString('base64');
  const chunks = base64.match(/.{1,100}/g) ?? [''];

  return `// This file is generated by scripts/generate-homepage.mjs from ../../index.html.\n// Do not edit this file manually.\nexport const STATIC_HOME_PAGE_BASE64 = [\n${chunks
    .map((chunk) => `  '${chunk}',`)
    .join('\n')}\n].join('');\n`;
}

const html = readFileSync(sourcePath, 'utf8');
const nextOutput = renderOutput(html);

let currentOutput = '';
try {
  currentOutput = readFileSync(outputPath, 'utf8');
} catch {}

if (checkOnly) {
  if (currentOutput !== nextOutput) {
    console.error(
      '[homepage] src/runtime/page.static.ts is out of sync with index.html. Run `pnpm run generate:homepage`.'
    );
    process.exit(1);
  }

  console.log('[homepage] page.static.ts is in sync.');
  process.exit(0);
}

if (currentOutput !== nextOutput) {
  writeFileSync(outputPath, nextOutput, 'utf8');
  console.log('[homepage] Updated src/runtime/page.static.ts.');
} else {
  console.log('[homepage] page.static.ts already up to date.');
}
